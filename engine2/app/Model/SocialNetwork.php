	public function beforeSave($options = array()){
		$data = $this->data;
		if(!empty($data['SocialNetwork']['title'])){
			$slug = $this->slugify($data['SocialNetwork']['title']);
			$check_slug = $this->find('count',array('conditions'=>array('slug'=>$slug)));
			if($check_slug == 0){
				$data['SocialNetwork']['slug'] = $slug;
			
			}else{
				$counter = $check_slug+1;
				$data['SocialNetwork']['slug'] = $slug."_{$counter}";
			}
			$this->data = $data;
			
			return true;
		}
		
	}
